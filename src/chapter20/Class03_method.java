package chapter20;

/*
 * 메서드 : 기능을 구현, 하나의 기능
 * 메서드 선언 : 접근 제한자   반환 타입   메서드 이름(매개변수) {}
 * 메서드 이름 : 카멜식, 동사형 권장
 * 반환 타입(리턴타입) : void / 데이터 타입
 * 매개변수 유뮤, return 유무에 따라 나뉨
 */
public class Class03_method {
	
	//메서드
	static public void hello() {
		System.out.println("안녕하세요");
	}
	
	public static void main(String[] args) {
		
		Class03_method.hello(); //안녕하세요
		
	}
	
	
	
	

}
